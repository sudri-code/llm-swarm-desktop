"""app/styles — design token helpers for PySide6.

Generated artefacts (tokens.qss, tokens.py) are produced by
``tools/build_qss.py`` / ``make tokens`` and are .gitignore'd.
This module provides a stable import surface that survives the
generated files not yet existing (e.g. fresh checkout before
``make tokens``).
"""

from __future__ import annotations

from pathlib import Path

_STYLES_DIR = Path(__file__).resolve().parent


def load_qss() -> str:
    """Return the contents of the generated tokens.qss stylesheet.

    Raises ``FileNotFoundError`` with a helpful message if the file has not
    been generated yet (run ``make tokens``).
    """
    qss_path = _STYLES_DIR / "tokens.qss"
    if not qss_path.exists():
        raise FileNotFoundError(
            f"tokens.qss not found at {qss_path}. "
            "Run `make tokens` to generate it from the webclient design tokens."
        )
    return qss_path.read_text(encoding="utf-8")


def load_pairing_qss() -> str:
    """Return the contents of the pairing-specific stylesheet.

    Unlike ``tokens.qss``, ``pairing.qss`` is a hand-authored file that
    lives alongside the generated tokens.  It is appended to ``tokens.qss``
    by ``apply_to_app()``.

    Returns an empty string if the file does not exist (graceful degradation).
    """
    pairing_qss_path = _STYLES_DIR / "pairing.qss"
    if not pairing_qss_path.exists():
        return ""
    return pairing_qss_path.read_text(encoding="utf-8")


def apply_to_app(app: object) -> None:
    """Load and apply tokens.qss + pairing.qss to a QApplication instance.

    Usage::

        from PySide6.QtWidgets import QApplication
        from app.styles import apply_to_app

        qt_app = QApplication(sys.argv)
        apply_to_app(qt_app)

    Also registers bundled fonts (Inter, JetBrains Mono) via Qt resource
    system if ``resources_rc`` is available.
    """
    # Register fonts from Qt resource bundle (generated by pyside6-rcc)
    try:
        from PySide6.QtGui import QFontDatabase

        from app.resources import resources_rc as _  # noqa: F401

        for font_path in [
            ":/fonts/Inter-Variable.ttf",
            ":/fonts/JetBrainsMono-Regular.ttf",
            ":/fonts/JetBrainsMono-Medium.ttf",
        ]:
            QFontDatabase.addApplicationFont(font_path)
    except ImportError:
        # resources_rc not yet generated — fonts fall back to system fonts
        pass

    # Apply stylesheet: base tokens + pairing extensions
    stylesheet = load_qss() + "\n" + load_pairing_qss()
    # app is QApplication but we avoid the import at module level to keep
    # this module importable in non-Qt contexts (tests, build scripts)
    app.setStyleSheet(stylesheet)  # type: ignore[union-attr]
